/**
 * Váriaveis:
 * Armazena um valor, e pode ser declarada como let ou const.
 * Let: permite que o valor seja substituido
 * Const: o valor é fixo, e não pode ser substituido
 */
/**
 * @param  {} variavel
 */
let variavel = 10 / 10;
console.log(variavel);
/**
 * @param  {} variavel2
 */
const variavel2 = 20 / 10;
console.log(variavel2);
